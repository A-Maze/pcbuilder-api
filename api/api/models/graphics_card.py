from mongoengine import StringField
from models.hardware import Hardware


class GraphicsCard(Hardware):
    videochip = StringField(max_length=200)
    chipset_generation = StringField(max_length=200)
    video_chip_manufacturer = StringField(max_length=200)
    rated_speed_video_chip = StringField(max_length=200)
